package Keywords;

public class DoWhile {
	public static void main(String args[])
	{
        int arr[]={4,24,18,36};
        //i starts with 0 as array index starts with 0
        int i=0;
        do
        {
             System.out.println(arr[i]);
             i++;
        }
        while(i<4);
     }
}
